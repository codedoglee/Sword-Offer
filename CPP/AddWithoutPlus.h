#ifndef ADDWITHOUTPLUS_H_
#define ADDWITHOUTPLUS_H_

/*
 * 题目：写一个函数，求两个整数之和，要求在函数体内不得使用+、-、*、/四则运算。
 */
#include <stdio.h>

int Add(int num1, int num2) {
	int sum, carry;
	do {
		sum = num1 ^ num2;
		carry = (num1 & num2) << 1;

		num1 = sum;
		num2 = carry;
	}
	while (num2 != 0);
	return sum;
}

void AddTest() {
	int num1 = 10;
	int num2 = 777;
	printf("%d\n", Add(num1, num2));
}


#endif /* ADDWITHOUTPLUS_H_ */
